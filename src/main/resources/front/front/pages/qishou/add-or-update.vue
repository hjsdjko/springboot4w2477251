	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				qishouzhanghao: '',
				qishouxingming: '',
				mima: '',
				xingbie: '',
				lianxidianhua: '',
				gongzuozhuangtai: '',
				zhaopian: '',
			        yue: Number('0') ,
				tijiaorenzheng: '',
				yonghuxieyi: '',
				shhf: '',
				discussnum: '',
				openid: '',
				nickname: '',
				avatarurl: '',
				},
				xingbieOptions: [],
				xingbieIndex: 0,
				gongzuozhuangtaiOptions: [],
				gongzuozhuangtaiIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   qishouzhanghao : false,
                   qishouxingming : false,
                   mima : false,
                   xingbie : false,
                   lianxidianhua : false,
                   gongzuozhuangtai : false,
                   zhaopian : false,
                   yue : false,
                   tijiaorenzheng : false,
                   yonghuxieyi : false,
                   sfsh : false,
                   shhf : false,
                   discussnum : false,
                   openid : false,
                   nickname : false,
                   avatarurl : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取

            this.ro.yue = true;
            this.ro.yonghuxieyi = true;

			// 自定义下拉框值
			this.xingbieOptions = "男,女".split(',')
			// 自定义下拉框值
			this.gongzuozhuangtaiOptions = "空闲,忙碌".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`qishou`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='qishouzhanghao'){
					this.ruleForm.qishouzhanghao = obj[o];
					this.ro.qishouzhanghao = true;
					continue;
					}
					if(o=='qishouxingming'){
					this.ruleForm.qishouxingming = obj[o];
					this.ro.qishouxingming = true;
					continue;
					}
					if(o=='mima'){
					this.ruleForm.mima = obj[o];
					this.ro.mima = true;
					continue;
					}
					if(o=='xingbie'){
					this.ruleForm.xingbie = obj[o];
					this.ro.xingbie = true;
					continue;
					}
					if(o=='lianxidianhua'){
					this.ruleForm.lianxidianhua = obj[o];
					this.ro.lianxidianhua = true;
					continue;
					}
					if(o=='gongzuozhuangtai'){
					this.ruleForm.gongzuozhuangtai = obj[o];
					this.ro.gongzuozhuangtai = true;
					continue;
					}
					if(o=='zhaopian'){
					this.ruleForm.zhaopian = obj[o].split(",")[0];
					this.ro.zhaopian = true;
					continue;
					}
					if(o=='yue'){
					this.ruleForm.yue = obj[o];
					this.ro.yue = true;
					continue;
					}
					if(o=='tijiaorenzheng'){
					this.ruleForm.tijiaorenzheng = obj[o];
					this.ro.tijiaorenzheng = true;
					continue;
					}
					if(o=='yonghuxieyi'){
					this.ruleForm.yonghuxieyi = obj[o];
					this.ro.yonghuxieyi = true;
					continue;
					}
					if(o=='discussnum'){
					this.ruleForm.discussnum = obj[o];
					this.ro.discussnum = true;
					continue;
					}
					if(o=='openid'){
					this.ruleForm.openid = obj[o];
					this.ro.openid = true;
					continue;
					}
					if(o=='nickname'){
					this.ruleForm.nickname = obj[o];
					this.ro.nickname = true;
					continue;
					}
					if(o=='avatarurl'){
					this.ruleForm.avatarurl = obj[o];
					this.ro.avatarurl = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
			
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
            yonghuxieyiChange(e) {
                this.ruleForm.yonghuxieyi = e
            },
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
