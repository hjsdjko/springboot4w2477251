<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				gonggaobianhao: this.getUUID(),
				gonggaobiaoti: '',
				gonggaozhaopian: '',
				gonggaojianjie: '',
				gonggaoneirong: '',
				fuwuleixing: '',
				zhanghao: '',
				xingming: '',
				shhf: '',
				discussnum: '',
				},
				fuwuleixingOptions: [],
				fuwuleixingIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   gonggaobianhao : false,
                   gonggaobiaoti : false,
                   gonggaozhaopian : false,
                   gonggaojianjie : false,
                   gonggaoneirong : false,
                   fuwuleixing : false,
                   zhanghao : false,
                   xingming : false,
                   sfsh : false,
                   shhf : false,
                   discussnum : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.zhanghao = this.user.zhanghao
			this.ro.zhanghao = true;
			this.ruleForm.xingming = this.user.xingming
			this.ro.xingming = true;


			// 下拉框
			res = await this.$api.option(`fuwuleixing`,`fuwuleixing`,{});
			this.fuwuleixingOptions = res.data;
            this.fuwuleixingOptions.unshift("请选择服务类型");

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("appUserid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = Number(options.refid);
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`xinxifuwuliulan`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='gonggaobianhao'){
					this.ruleForm.gonggaobianhao = obj[o];
					this.ro.gonggaobianhao = true;
					continue;
					}
					if(o=='gonggaobiaoti'){
					this.ruleForm.gonggaobiaoti = obj[o];
					this.ro.gonggaobiaoti = true;
					continue;
					}
					if(o=='gonggaozhaopian'){
					this.ruleForm.gonggaozhaopian = obj[o].split(",")[0];
					this.ro.gonggaozhaopian = true;
					continue;
					}
					if(o=='gonggaojianjie'){
					this.ruleForm.gonggaojianjie = obj[o];
					this.ro.gonggaojianjie = true;
					continue;
					}
					if(o=='gonggaoneirong'){
					this.ruleForm.gonggaoneirong = obj[o];
					this.ro.gonggaoneirong = true;
					continue;
					}
					if(o=='fuwuleixing'){
					this.ruleForm.fuwuleixing = obj[o];
					this.ro.fuwuleixing = true;
					continue;
					}
					if(o=='zhanghao'){
					this.ruleForm.zhanghao = obj[o];
					this.ro.zhanghao = true;
					continue;
					}
					if(o=='xingming'){
					this.ruleForm.xingming = obj[o];
					this.ro.xingming = true;
					continue;
					}
					if(o=='discussnum'){
					this.ruleForm.discussnum = obj[o];
					this.ro.discussnum = true;
					continue;
					}
				}
			}
			this.styleChange()
            this.$forceUpdate()
			
			if (uni.getStorageSync('raffleType') && uni.getStorageSync('raffleType') != null) {
				uni.removeStorageSync('raffleType')
				setTimeout(() => {
					this.onSubmitTap()
				}, 300)
			}
		},
		methods: {
			styleChange() {
				this.$nextTick(()=>{
					// document.querySelectorAll('.app-update-pv . .uni-input-input').forEach(el=>{
					//   el.style.backgroundColor = this.addUpdateForm.input.content.backgroundColor
					// })
				})
			},

			// 多级联动参数



			// 下拉变化
			fuwuleixingChange(e) {
				this.fuwuleixingIndex = e.target.value
				this.ruleForm.fuwuleixing = this.fuwuleixingOptions[this.fuwuleixingIndex]
			},

			gonggaozhaopianTap() {
				let _this = this;
				this.$api.upload(function(res) {
					_this.ruleForm.gonggaozhaopian = 'upload/' + res.file;
					_this.$forceUpdate();
					_this.$nextTick(()=>{
						_this.styleChange()
					})
				});
			},

			getUUID () {
				return new Date().getTime();
			},
			async onSubmitTap() {
				if(this.ruleForm.gonggaobianhao){
					this.ruleForm.gonggaobianhao = String(this.ruleForm.gonggaobianhao)
				}
//跨表计算判断
				var obj;
				if(this.ruleForm.discussnum&&(!this.$validate.isIntNumer(this.ruleForm.discussnum))){
					this.$utils.msg(`评论数应输入整数`);
					return
				}
				//更新跨表属性
				var crossuserid;
				var crossrefid;
				var crossoptnum;
				if(this.cross){
                    uni.setStorageSync('crossCleanType',true);
					var statusColumnName = uni.getStorageSync('statusColumnName');
					var statusColumnValue = uni.getStorageSync('statusColumnValue');
					if(statusColumnName!='') {
                        if(!obj) {
						    obj = uni.getStorageSync('crossObj');
                        }
						if(!statusColumnName.startsWith("[")) {
							for (var o in obj){
								if(o==statusColumnName){
									obj[o] = statusColumnValue;
								}

							}
							var table = uni.getStorageSync('crossTable');
							await this.$api.update(`${table}`, obj);
						} else {
						       crossuserid=Number(uni.getStorageSync('appUserid'));
						       crossrefid=obj['id'];
						       crossoptnum=uni.getStorageSync('statusColumnName');
						       crossoptnum=crossoptnum.replace(/\[/,"").replace(/\]/,"");
						}
					}
				}
				if(crossrefid && crossuserid) {
					this.ruleForm.crossuserid=crossuserid;
					this.ruleForm.crossrefid=crossrefid;
					let params = {
						page: 1,
						limit:10,
						crossuserid:crossuserid,
						crossrefid:crossrefid,
					}
					let res = await this.$api.list(`xinxifuwuliulan`, params);
					if (res.data.total >= crossoptnum) {
						this.$utils.msg(uni.getStorageSync('tips'));
                        uni.removeStorageSync('crossCleanType');
						return false;
					} else {
                //跨表计算
						if(this.ruleForm.id){
							await this.$api.update(`xinxifuwuliulan`, this.ruleForm);
						}else{
							await this.$api.add(`xinxifuwuliulan`, this.ruleForm);
						}
						this.$utils.msgBack('提交成功');
					}
				} else {
                //跨表计算
					if(this.ruleForm.id){
						await this.$api.update(`xinxifuwuliulan`, this.ruleForm);
					}else{
						await this.$api.add(`xinxifuwuliulan`, this.ruleForm);
					}
					this.$utils.msgBack('提交成功');
				}
			},
			optionsChange(e) {
				this.index = e.target.value
			},
			bindDateChange(e) {
				this.date = e.target.value
			},
			getDate(type) {
				const date = new Date();
				let year = date.getFullYear();
				let month = date.getMonth() + 1;
				let day = date.getDate();
				if (type === 'start') {
					year = year - 60;
				} else if (type === 'end') {
					year = year + 2;
				}
				month = month > 9 ? month : '0' + month;;
				day = day > 9 ? day : '0' + day;
				return `${year}-${month}-${day}`;
			},
			toggleTab(str) {
				if(this.ro[str]){
					return false
				}
				this.$refs[str].show();
			}
		}
	}
</script>

