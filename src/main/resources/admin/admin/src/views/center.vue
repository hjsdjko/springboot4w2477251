      qishougongzuozhuangtaiOptions: [],
    };
  },
  mounted() {
    var table = this.$storage.get("sessionTable");
    this.flag = table;
    this.$http({
      url: `${this.$storage.get("sessionTable")}/session`,
      method: "get"
    }).then(({ data }) => {
      if (data && data.code === 0) {
        this.ruleForm = data.data;
      } else {
        this.$message.error(data.msg);
      }
    });
    this.yonghuxingbieOptions = "男,女".split(',')
    this.qishouxingbieOptions = "男,女".split(',')
    this.qishougongzuozhuangtaiOptions = "空闲,忙碌".split(',')
  },
  methods: {
    yonghutouxiangUploadChange(fileUrls) {
        this.ruleForm.touxiang = fileUrls;
    },
    qishouzhaopianUploadChange(fileUrls) {
        this.ruleForm.zhaopian = fileUrls;
    },
    qishoutijiaorenzhengUploadChange(fileUrls) {
        this.ruleForm.tijiaorenzheng = fileUrls;
    },
	usersimageUploadChange(fileUrls) {
		this.ruleForm.image = fileUrls;
	},
    onUpdateHandler() {
      if((!this.ruleForm.zhanghao)&& 'yonghu'==this.flag){
        this.$message.error('账号不能为空');
        return
      }


      if((!this.ruleForm.mima)&& 'yonghu'==this.flag){
        this.$message.error('密码不能为空');
        return
      }








      if( 'yonghu' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
        this.$message.error(`手机应输入手机格式`);
        return
      }






        if(this.ruleForm.touxiang!=null) {
                this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
        }










      if((!this.ruleForm.qishouzhanghao)&& 'qishou'==this.flag){
        this.$message.error('骑手账号不能为空');
        return
      }


      if((!this.ruleForm.qishouxingming)&& 'qishou'==this.flag){
        this.$message.error('骑手姓名不能为空');
        return
      }


      if((!this.ruleForm.mima)&& 'qishou'==this.flag){
        this.$message.error('密码不能为空');
        return
      }






      if( 'qishou' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
        this.$message.error(`联系电话应输入手机格式`);
        return
      }




        if(this.ruleForm.zhaopian!=null) {
                this.ruleForm.zhaopian = this.ruleForm.zhaopian.replace(new RegExp(this.$base.url,"g"),"");
        }


      if( 'qishou' ==this.flag && this.ruleForm.yue&&(!isNumber(this.ruleForm.yue))){
        this.$message.error(`余额应输入数字`);
        return
      }


        if(this.ruleForm.tijiaorenzheng!=null) {
                this.ruleForm.tijiaorenzheng = this.ruleForm.tijiaorenzheng.replace(new RegExp(this.$base.url,"g"),"");
        }








      if( 'qishou' ==this.flag && this.ruleForm.discussnum&&(!isIntNumer(this.ruleForm.discussnum))){
       this.$message.error(`评论数应输入整数`);
        return
      }






      if('users'==this.flag && this.ruleForm.username.trim().length<1) {
	this.$message.error(`用户名不能为空`);
        return	
      }
	  if(this.flag=='users'){
	  	this.ruleForm.image = this.ruleForm.image.replace(new RegExp(this.$base.url,"g"),"")
	  }
      this.$http({
        url: `${this.$storage.get("sessionTable")}/update`,
        method: "post",
        data: this.ruleForm
      }).then(({ data }) => {
        if (data && data.code === 0) {
          this.$message({
            message: "修改信息成功",
            type: "success",
            duration: 1500,
            onClose: () => {
				if(this.flag=='users'){
					this.$storage.set('headportrait',this.ruleForm.image)
				}
            }
          });
        } else {
          this.$message.error(data.msg);
        }
      });
    }
  }
};
</script>
